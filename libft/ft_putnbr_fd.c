#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	res;

	res = n;
	if (res < 0)
	{
		ft_putchar_fd('-', fd);
		res *= -1;
	}
	if (res >= 10)
		ft_putnbr_fd(res / 10, fd);
	ft_putchar_fd((res % 10) + '0', fd);
}
